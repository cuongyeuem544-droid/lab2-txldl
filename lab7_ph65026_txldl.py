    skew_val = df[col].skew()
    q1 = df[col].quantile(0.25)
    q3 = df[col].quantile(0.75)
    iqr = q3 - q1
    lower = q1 - 1.5 * iqr
    upper = q3 + 1.5 * iqr
    outliers = df[(df[col] < lower) | (df[col] > upper)][col].count()

    if skew_val > 1:
        trend = "lệch phải mạnh"
        suggest = "log / Box-Cox / Power"
    elif skew_val > 0.5:
        trend = "lệch phải vừa"
        suggest = "log hoặc Power"
    elif skew_val < -1:
        trend = "lệch trái mạnh"
        suggest = "Yeo-Johnson / Power"
    elif skew_val < -0.5:
        trend = "lệch trái vừa"
        suggest = "Power"
    else:
        trend = "gần đối xứng"
        suggest = "không cần transform mạnh"

    print(f"\nCột: {col}")
    print(f"  - Skewness: {skew_val:.4f}")
    print(f"  - Xu hướng: {trend}")
    print(f"  - Số outlier (IQR): {outliers}")
    print(f"  - Gợi ý biến đổi: {suggest}")

