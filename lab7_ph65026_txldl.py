def transform_for_business(data, col):
    s = data[col].dropna()

    if (s > 0).all():
        transformed = np.log(s)
        method = "log"
    else:
        pt = PowerTransformer(method="yeo-johnson")
        transformed = pt.fit_transform(s.values.reshape(-1, 1)).flatten()
        method = "power"

    return transformed, method

