# Hàm vẽ Histogram + KDE
def plot_hist_kde(data, col, title_suffix=""):
    plt.figure(figsize=(8, 5))
    plt.hist(data[col].dropna(), bins=30, density=True, alpha=0.6)

    kde = stats.gaussian_kde(data[col].dropna())
    x_vals = np.linspace(data[col].min(), data[col].max(), 300)
    plt.plot(x_vals, kde(x_vals))

    plt.title(f"Histogram + KDE của {col} {title_suffix}")
    plt.xlabel(col)
    plt.ylabel("Density")
    plt.grid(True, alpha=0.3)
    plt.show()

