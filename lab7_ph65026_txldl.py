compare_rows = []

def compare_transform_positive(data, col):
    temp = data[[col]].dropna().copy()

    # Log
    temp["log"] = np.log(temp[col])

    # Box-Cox
    boxcox_values, best_lambda = stats.boxcox(temp[col])
    temp["boxcox"] = boxcox_values

    skew_before = temp[col].skew()
    skew_log = temp["log"].skew()
    skew_boxcox = temp["boxcox"].skew()

    compare_rows.append({
        "Column": col,
        "Skew_before": skew_before,
        "Skew_after_Log": skew_log,
        "Skew_after_BoxCox": skew_boxcox,
        "Skew_after_Power": np.nan,
        "Best_Method": "Log" if abs(skew_log) < abs(skew_boxcox) else "Box-Cox",
        "Lambda_BoxCox": best_lambda,
        "Comment": "Cột dương hoàn toàn, phù hợp log và Box-Cox"
    })

    # Vẽ trước - sau
    fig, axes = plt.subplots(1, 3, figsize=(18, 4))

    axes[0].hist(temp[col], bins=30, alpha=0.7)
    axes[0].set_title(f"{col} - Gốc")

    axes[1].hist(temp["log"], bins=30, alpha=0.7)
    axes[1].set_title(f"{col} - Log")

    axes[2].hist(temp["boxcox"], bins=30, alpha=0.7)
    axes[2].set_title(f"{col} - Box-Cox")

    plt.tight_layout()
    plt.show()

    print(f"\nCột {col}")
    print(f"Skew trước: {skew_before:.4f}")
    print(f"Skew sau Log: {skew_log:.4f}")
    print(f"Skew sau Box-Cox: {skew_boxcox:.4f}")
    print(f"Lambda tối ưu Box-Cox: {best_lambda:.4f}")

def compare_transform_mixed(data, col):
    temp = data[[col]].dropna().copy()

    pt = PowerTransformer(method="yeo-johnson")
    temp["power"] = pt.fit_transform(temp[[col]])

    skew_before = temp[col].skew()
    skew_power = temp["power"].skew()

    compare_rows.append({
        "Column": col,
        "Skew_before": skew_before,
        "Skew_after_Log": np.nan,
        "Skew_after_BoxCox": np.nan,
        "Skew_after_Power": skew_power,
        "Best_Method": "Yeo-Johnson Power",
        "Lambda_BoxCox": np.nan,
        "Comment": "Cột có âm/0 nên dùng Yeo-Johnson"
    })

    fig, axes = plt.subplots(1, 2, figsize=(12, 4))

    axes[0].hist(temp[col], bins=30, alpha=0.7)
    axes[0].set_title(f"{col} - Gốc")

    axes[1].hist(temp["power"], bins=30, alpha=0.7)
    axes[1].set_title(f"{col} - Power (Yeo-Johnson)")

    plt.tight_layout()
    plt.show()

    print(f"\nCột {col}")
    print(f"Skew trước: {skew_before:.4f}")
    print(f"Skew sau Power: {skew_power:.4f}")

compare_transform_positive(df, pos_col1)
